const db = new Database(DB_PATH);

db.exec(`
  CREATE TABLE IF NOT EXISTS disc_plastics (
    manufacturer_key  TEXT NOT NULL,
    manufacturer      TEXT NOT NULL,
    plastic           TEXT NOT NULL,
    tier              TEXT NOT NULL CHECK(tier IN ('Premium', 'Baseline')),
    PRIMARY KEY (manufacturer_key, plastic)
  )
`);

const existing = db.prepare('SELECT COUNT(*) as n FROM disc_plastics').get();
if (existing.n > 0) {
  console.log(`disc_plastics already has ${existing.n} rows — skipping seed`);
  process.exit(0);
}

const insert = db.prepare(
  'INSERT OR REPLACE INTO disc_plastics (manufacturer_key, manufacturer, plastic, tier) VALUES (?, ?, ?, ?)'
);
const insertMany = db.transaction(rows => {
  for (const [manufacturer, plastic, tier] of rows) {
    insert.run(normalize(manufacturer), manufacturer, plastic, tier);
  }
});

insertMany(RECORDS);

// Alias short names used in flight_numbers for MVP/Axiom/Streamline
const alias = db.prepare(`
  INSERT OR IGNORE INTO disc_plastics (manufacturer_key, manufacturer, plastic, tier)
  SELECT ?, ?, plastic, tier FROM disc_plastics WHERE manufacturer_key = ?
`);
alias.run('mvp', 'MVP', 'mvp disc sports');
alias.run('axiom', 'Axiom', 'axiom discs');
alias.run('streamline', 'Streamline', 'streamline discs');

console.log(`Created disc_plastics and inserted ${RECORDS.length} rows (+ MVP/Axiom/Streamline aliases)`);
